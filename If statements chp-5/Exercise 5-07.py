# Make a list of your favorite fruits, and then write a series of independent if statements that check for certain fruits in your list.
favourite_fruits = ['apple','banana','orange','watermelon','mango']
for favourite_fruit in favourite_fruits:
    if "apple" in favourite_fruit:
        print("I really like apples.")
    elif "banana" in favourite_fruit:
        print("I really like bananas.")
    elif "orange" in favourite_fruit:
        print("I really like oranges")
    elif "watermelon" in favourite_fruit:
        print("I really like watermelon")
    elif "mango" in favourite_fruit:
        print("I really like mango")
